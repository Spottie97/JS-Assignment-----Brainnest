/*Large int with if-else*/
const num1 = 5;
const num2 = 10;
if (num1 > num2) {
  console.log(num1 + " is larger");
} else {
  console.log(num2 + " is larger");
}

/*Check if Even or Odd int*/
const num = 5;
if (num % 2 === 0) {
  console.log(num + " is an even number");
} else {
  console.log(num + " is an odd number");
}
